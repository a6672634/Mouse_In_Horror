package org.itstep.qa.lesson;

public class Bush {
    private int x;
    private int y;
    private String color;

    public Bush(int x, int y, String color) {
        this.x = x;
        this.y = y;
        this.color = color;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String getColor() {
        return color;
    }
}
